!This is a module file that constains dynamical parameters
! like grid points, dimension of 2D phase space, time of
! simulation etc. Constants are defined as well here.
! L, M is the dimension of the 2D grid.
! Need to create POTFUNC input file in a grid of L, M.
! See (README.md)
!
!
MODULE CONST
IMPLICIT NONE

REAL*8, PARAMETER:: ANG2BOHR = 1.8897259886D0
REAL*8, PARAMETER:: EV2HARTREE = 1 / 27.21138602D0
REAL*8, PARAMETER:: PI = 4.D0*DATAN(1.0D0)
REAL*8, PARAMETER:: HBAR=1.0D0
REAL*8, PARAMETER:: AU2FS = 0.02418884254D0
REAL*8, PARAMETER:: MP2ME = 1836.15267343D0

!DIMENSION OF THE GRID
INTEGER, PARAMETER:: L=60, M=420
!GRID MINIMA AND MAXIMA (X, Y) (L, M) Dimension
REAL*8, PARAMETER:: XMIN= 2.0D0, XMAX= 8.0D0
REAL*8, PARAMETER:: YMIN= 0.0D0, YMAX= 50.0D0

!NO OF. KINETIC ENERGY DATA POINTS
INTEGER, PARAMETER:: NEKIN=100
!KINETIC ENERGY SPECTRUM RANGE
REAL*8, PARAMETER:: EMAX=0.10D0, EMIN=0.00D0
!MAXIMUM TIME OF SIMULATION (FS)
REAL*8, PARAMETER:: T_MAX = 800.0
!TIME INTERVAL IN FS
REAL*8, PARAMETER:: dT= 1.00D0/AU2FS
!TOTAL TIME STEPS
INTEGER, PARAMETER:: NSTEPS=INT(T_MAX/(dT*AU2FS))+1
!REDUCED MASS ALONG X-DIRECTION
REAL*8, PARAMETER:: XM_X=3.5030D0*MP2ME
!REDUCED MASS ALONG Y-DIRECTION
REAL*8, PARAMETER:: XM_Y=3.1130D0*MP2ME


END MODULE CONST
